#Write a program to perform add,sub,mul,div,modulo div on two floating point numbers
num1 = float(input("Enter the first floating number\t "))
num2 = float(input("Enter the second floating point number\t "))
print(f"The add of these two numbers will be {num1+num2}")
print(f"The sub of these two numbers will be {num1-num2}")
print(f"The mul of these two numbers will be {num1*num2}")
print(f"The div of these two numbers will be {num1/num2}")
print(f"The  modulo div of these two numbers will be {num1%num2}")