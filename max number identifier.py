"""Write a program to find the greatest number from three numbers entered by the user """
num1 = int(input('Enter the first number :\t'))
num2 = int(input('Enter the second number :\t'))
num3 = int(input('Enter the third number :\t'))
print(f"The maximum number out of {num1} , {num2} , {num3} is {(max(num1,num2,num3))}")

