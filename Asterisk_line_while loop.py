""" There are 3 stages of while loop
first initializaation in this step we initialize the value for the loop
second is conditionalizing in this step we give a condition to the loop
third is increment and decrementalization in this we create a value so that the value can be repeated so that the condition can be repeated until the condition changes
"""


"""Write a program o print 20 horizontal Asterisks"""
#Intialization
