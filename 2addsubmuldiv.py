#Write a program to perform addition subtraction multiplication division integer division and modulo division on two integer numbers
num1 = int(input("Enter the first Number:\t"))
num2 = int(input("Enter the second Number:\t"))
print(f"The addition of these two numbers will be {num1+num2}")
print(f"The subtraction of these two numbers will be {num1-num2}")
print(f"The multiplication  of these two numbers will be {num1*num2}")
print(f"The division of these two numbers will be {num1/num2}")
print(f"The modulo division of these two numbers will be {num1%num2}")
