#Write a program that demonstrates the use of relational operators
num1 = int(input("Give me first of two number so that i can demonstrate the relational operators:\t"))
num2 = int(input("Give me second of two number so that i can demonstrate the relational operators:\t"))
print(f"the < operator will return {num1<num2}")
print(f"the > operator will return {num1>num2}")
print(f"the != operator will return {num1!=num2}")
print(f"the >= operator will return {num1>=num2}")
print(f"the <= operator will return {num1<=num2}")
print(f"the == operator will return {num1==num2}")
